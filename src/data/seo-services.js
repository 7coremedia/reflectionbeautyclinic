export const getServiceByPath = (path) => {
  for (const cat of categoriesData) {
    const service = cat.services.find(s => s.path === path || s.id === path.split('/').pop());
    if (service) {
      service.parentCategory = { id: cat.id, title: cat.h1, path: cat.path };
      return service;
    }
  }
  return null;
};

export const getCategoryById = (id) => {
  return categoriesData.find(cat => cat.id === id);
};
